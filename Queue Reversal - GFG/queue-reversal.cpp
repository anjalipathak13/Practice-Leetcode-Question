//Function to reverse the queue.
class Solution
{
    public:
    queue<int> rev(queue<int> q)
    {   int n=q.size();
        vector<int>v(n);
        int i=n-1;
        while(!q.empty()){
            int temp=q.front();
            q.pop();
            v[i--] = temp;
        }
        
        for(int i=0;i<v.size();i++){
            q.push(v[i]);
        }
        return q;
    }
};


