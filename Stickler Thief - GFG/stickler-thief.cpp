class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        // Your code here
         int p2=0,p1=0;
        for(int i=0;i<n;i++){
            int curr=max(p1,arr[i]+p2);
            p2=p1;
            p1=curr;
        }
        return p1;

    }
};

